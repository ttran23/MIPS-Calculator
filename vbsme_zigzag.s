			slt		$t9, $s4, $s1			# (j + 1) < y?		#i just did a full increment, and then decrement again if it false
            addi	$s4, $s4, 1				# j + 1				#in mips, idk how to do a temp increment, so
			bne		$t9, $zero, CalcuSAD	# if j+1 < y, then calculate with [i][j+1] 
			#####################################
			#sub		$s4, $s4, 1				# decrement j back. check down now
			addi	$s4, $s4, -1
			#####################################
			slt		$t9, $s3, $s0			# (i + 1) < x?
            addi	$s3, $s3, 1				# i + 1
			bne		$t9, $zero, CalcuSAD	# if (i+1) < x, then calculate with [i+1][j]
			#####################################
			#sub		$s3, $s3, 1				# decrement i back, 
			addi	$s3, $s3, -1
			#####################################
			j		EXIT					#if can no longer move right or down, then at the end
	
	MoveDiagDown:	#move diagonal down			indexTYPE = 1
					#if (j - 1 >= 0 && i + 1 < n) 
					#{
					#	i++;	# down row
					#	j--;	# left column
					#}
			
			#####################################
			#sub		$s4, $s4, 1			# j - 1
			addi		$s4, $s4, -1
			#####################################
			addi	$s3, $s3, 1			# i + 1
			######################################
			#sge		$t9, $s4, $zero		# (j-1) >= 0?
			#beq		$t9, $zero, FixIJ1	# fails AND condition, fix both i and j
			slt		$t9, $s4, $zero		# 0 < (j-1)?
			bne		$t9, $zero, FixIJ1
			######################################
			#sle		$t9, $s3, $s0		# (i+1) < x 	AKA not the last row
			#beq		$t9, $zero, FixIJ1	# fails AND condition, fix both i and j
			slt			$t9, $s3, $s0
			beq			$t9, $zero, SSLELoc
			######################################
			j		CalcuSAD
	
