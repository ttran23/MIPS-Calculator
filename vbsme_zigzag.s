			#add		$s7, $s6, $zero			# $s7 = $s6, minSAD = curr SAD
			slt			$t9, $s6, $s7
			beq			$t9, $zero, FirstSLELocation
	skip:	add			$s7, $s6, $zero
			#####################################
			add		$v0, $s3, $zero
			add		$v1, $s4, $zero
			
	FrameMove:	# algorithm to move along frame
				# checks if current iteration has 
				# reached total iterations needed
			add     $t9, $s3, $s4
			
			# $s3 = i for frame traversal
			# $s4 = j for frame traversal			

	CheckDiag:			
			#checking where to go back to into the loop below
			######################################
			#seq		$t9, $t3, 1		 
			#bne		$t9, $zero, MoveDiagDown
			#seq		$t9, $t3, 2
			#bne		$t9, $zero, MoveDiagUp
			addi	$t9, $zero, 1 
			beq		$t9, $t3, MoveDiagDown
			addi	$t9, $zero, 2 
			beq		$t9, $t3, MoveDiagUp			
			######################################
	MoveRightDown:	#Check move right one step, then down, indexTYPE = 0
					# 
