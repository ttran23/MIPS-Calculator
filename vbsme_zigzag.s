			slt		$t9, $s3, $s0			# (i + 1) < x?
            addi	$s3, $s3, 1				# i + 1
			bne		$t9, $zero, CalcuSAD	# if (i+1) < x, then calculate with [i+1][j]
			#####################################
			#sub		$s3, $s3, 1				# decrement i back, 	
			addi	$s3, $s3, -1
			#####################################
			slt		$t9, $s4, $s1			# (j + 1) < y?	
            addi	$s4, $s4, 1				# j + 1	
			bne		$t9, $zero, CalcuSAD	# if j+1 < y, then calculate with [i][j+1] 
			#####################################
			#sub		$s4, $s4, 1				# decrement j back. check down now
			addi 	$s4, $s4, -1
			#####################################
			j		EXIT					#if can no longer move right or down, then at the end
	
