			#####################################
			######################################
			#sge		$t9, $s3, $zero		# (i-1) >= 0?
			#beq		$t9, $zero, FixIJ2	# fails AND condition, fix both i and j
			slt		$t9, $s3, $zero		# 0 < (j-1)?
			bne		$t9, $zero, FixIJ2
			######################################
			#sle		$t9, $s4, $s1		# (j+1) < y 	AKA not the last row
			#beq		$t9, $zero, FixIJ2	# fails AND condition, fix both i and j
			slt			$t9, $s4, $s1
			beq			$t9, $zero, TSLELoc
			######################################
			j		CalcuSAD
	
	FixIJ1:
	    	addi	$s4, $s4, 1		# this branch is when you can't move diagonal down
			#####################################
			#sub		$s3, $s3, 1		# that means it either hit the left or bottom edge
			addi	$s3, $s3, -1
			#####################################
			addi	$t3, $zero, 2	#
			j		MoveDownRight	# so jump to command to check to move down(if left edge) or right(if bottom edge)
			
	FixIJ2:
            add $t2, $zero, $zero
			#####################################
            #sub		$s4, $s4, 1		# similar to FixIJ1
			addi	$s4, $s4, -1
			#####################################
			addi	$s3, $s3, 1		# means it either hit top or right edge
			addi	$t3, $zero, 1	# 
			j 		MoveRightDown	# jump to command to check to move right and down

	EXIT:	#done with zig-zag search
			j		EXIT

	FirstSLELocation:
            beq $s6, $s7, skip
            j FrameMove
    SSLELoc:
           beq $s3, $s0, CalcuSAD
           j FixIJ1 
    TSLELoc:
           beq $s4, $s1, CalcuSAD
           j FixIJ2